class Candidate {
  final String name;
  final int age;
  final String position;
  final String party;
  final String manifesto;
  final String imageUrl;
  final String currentOffice;

  Candidate({
    required this.name,
    required this.age,
    required this.position,
    required this.party,
    required this.manifesto,
    required this.currentOffice,
    required this.imageUrl,
  });
}
